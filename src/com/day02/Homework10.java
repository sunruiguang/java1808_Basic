package com.day02;

public class Homework10 {
	public static void main(String[] args) {
		int i = 0;
		int j = 0;
		System.out.println(i);
		System.out.println(j);
		i++;
		++j;
		System.out.println(i);
		System.out.println(j);
		System.out.println("------------");
		System.out.println(i++);
		System.out.println(++j);
		System.out.println("================");
		System.out.println(i);
		System.out.println(j);
	}
}
